import nodemailer from "nodemailer"

const transporter = nodemailer.createTransport({
  service: 'gmail',
  auth: {
    type: 'OAuth2',
    user: process.env.EMAIL_USER,
    clientId: process.env.CLIENT_ID,
    clientSecret: process.env.CLIENT_SECRET,
    refreshToken: process.env.REFRESH_TOKEN,
  },
});

// Verify the connection configuration
transporter.verify((error, success) => {
  if (error) {
    console.error('Error connecting to email server:', error);
  } else {
    console.log('Email server is ready to send messages');
  }
});

// Function to send email
const sendEmail = async (to, subject, text, html) => {
  try {
    const info = await transporter.sendMail({
      from: `LedgerFlow <${process.env.EMAIL_USER}>`, // sender address
      to, // list of receivers
      subject, // Subject line
      text, // plain text body
      html, // html body
    });

    console.log('Message sent: %s', info.messageId);
    console.log('Preview URL: %s', nodemailer.getTestMessageUrl(info));
  } catch (error) {
    console.error('Error sending email:', error);
  }
};

export const sendRegistrationEmail = async (userEmail, name) => {
    const subject = 'Welcome to LedgerFlow!';
    const text = `Hi ${name},\n\nThank you for registering with LedgerFlow. We're excited to have you on board!\n\nBest regards,\nThe LedgerFlow Team`;
    const html = `<p>Hi ${name},</p><p>Thank you for registering with <strong>LedgerFlow</strong>. We're excited to have you on board!</p><p>Best regards,<br>The LedgerFlow Team</p>`;
    await sendEmail(userEmail, subject, text, html);
}

